package com.example.nutrihawk;

public class MineralSet {
	private int potassium;
	private int chlorine;
	private int sodium;
	private int calcium;
	private int phosphorus;
	private int magnesium;
	private int zinc;
	private int iron;
	private int manganese;
	private int copper;
	private int iodine;
	private int selenium;
	private int molybdenum;
	
	public MineralSet(int newPotassium, int newChlorine, int newSodium, int newCalcium, 
			int newPhosphorus, int newMagnesium, int newZinc, int newIron, int newManganese, 
			int newCopper, int newIodine, int newSelenium, int newMolybdenum) {
		potassium = newPotassium;
		chlorine = newChlorine;
		sodium = newSodium;
		calcium = newCalcium;
		phosphorus = newPhosphorus;
		magnesium = newMagnesium;
		zinc = newZinc;
		iron = newIron;
		manganese = newManganese;
		copper = newCopper;
		iodine = newIodine;
		selenium = newSelenium;
		molybdenum = newMolybdenum;
	}
}
